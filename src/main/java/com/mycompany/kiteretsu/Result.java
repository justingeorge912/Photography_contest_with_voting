    public Result(int cid) {
        initComponents();
        
       File file = new File("C:\\Users\\Justin\\Documents\\NetBeansProjects\\Kiteretsu\\src\\main\\java\\Images\\Result.jpg");
       BufferedImage bim = null;//new BufferedImage(jLabel2.getWidth(),jLabel2.getHeight(),BufferedImage.TYPE_INT_ARGB);
       try{
       bim = ImageIO.read(file);
       }
       catch(IOException e){}
       ImageIcon ic = new ImageIcon(bim);
       jLabel2.setIcon(ic);
        
         try{
                    this.cid=cid;
                    Connection con = DBHelper.getConnection();
                    Statement st = con.createStatement();
                    String sql = "SELECT name, email, image,caption FROM participants WHERE contest_id="+cid+" and vote_count = (SELECT MAX(vote_count) from participants)";
                    ResultSet res = st.executeQuery(sql);
                    while(res.next()){
                        byte[] img = res.getBytes("image");
                        ImageIcon image = new ImageIcon(img);
                        Image im = image.getImage();
                        Image myimg = im.getScaledInstance(imgLabel.getWidth(), imgLabel.getHeight(), Image.SCALE_SMOOTH);
                        ImageIcon newImage = new ImageIcon(myimg);
                        imgLabel.setIcon(newImage);
                        jLabel3.setText(res.getString("name"));
                        jLabel4.setText(res.getString("email"));
                        jLabel6.setText(res.getString("caption"));
                           
                    }
                    
         }
         catch(SQLException e){
             JOptionPane.showMessageDialog(null, e);
         }
         catch(Exception e){
             JOptionPane.showMessageDialog(null, e);
         }
    }

